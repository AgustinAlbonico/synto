def create_all_agents(
    router: LLMMultiProvider | None = None,
    memory_by_agent: dict | None = None,
) -> dict[str, BaseAgent]:
    """Factory: create all specialized agents with the given router."""
    if memory_by_agent is None:
        memory_by_agent = {}

    agents: dict[str, BaseAgent] = {}
    for cls in (
        HermesOrchestrator,
        CodeOrchestrator,
        BusinessAnalyst,
        ProductManager,
        Planner,
        CodebaseExplorer,
        Architect,
        SystemDesigner,
        Tester,
        BackendImplementer,
        FrontendImplementer,
        ContractAligner,
        Reviewer,
        SecurityReviewer,
        QAGatekeeper,
        DependencyChecker,
        TechnicalWriter,
        ReleaseManager,
        Builder,
    ):
        mem = memory_by_agent.get(cls.name, {}).get("items", 0)
        ctx = f"[Agent has {mem} memory items loaded]" if mem else ""
        agent = cls(router=router, memory_context=ctx)
        agents[cls.name] = agent
    return agents
